package ForkJoinPool;

class Page_ForkJoinPool {
    private String title;
    private String text;

    public Page_ForkJoinPool(String title, String text) {
        this.title = title;
        this.text = text;
    }

    public String getTitle() {
        return title;
    }

    public String getText() {
        return text;
    }
}
